<script setup lang="ts">
import { ref } from 'vue'
import { codeRules, mobileRules, passwordRules } from '@/utils/rules'
import { showSuccessToast, showToast } from 'vant'
import { loginByPassword } from '@/services/user'
import { useUserStore } from '../../stores/modules/user'
import { useRoute, useRouter } from 'vue-router'
const mobile = ref('')
const password = ref('')
const agree = ref(false)
const userStore = useUserStore()
const router = useRouter() // 获取路由对象
const route = useRoute() // 获取当前路由
const onSubmit = async () => {
  if (!agree.value) return showToast('请勾选协议')
  // 登录
  const res = await loginByPassword(mobile.value, password.value)
  userStore.setUser(res.data)
  showSuccessToast('登录成功')
  // as string 类型断言
  router.replace((route.query.returnUrl as string) || '/user') // 跳转
}
// 短信登录界面切换
const isPass = ref(true)
const code = ref('') // 短信验证码
</script>

<template>
  <div class="login-page">
    <cp-nav-bar right-text="注册" @click-right="$router.push('/register')"></cp-nav-bar>
    <!-- 头部 -->
    <div class="login-head">
      <h3>{{ isPass ? '密码登录' : '短信登录' }}</h3>
      <a href="javascript:;">
        <span @click="isPass = !isPass"> {{ isPass ? '短信验证码登录' : '密码登录' }} </span>
        <van-icon name="arrow"></van-icon>
      </a>
    </div>
    <!-- 表单 -->
    <van-form autocomplete="off" @submit="onSubmit">
      <van-field
        v-model="mobile"
        :rules="mobileRules"
        placeholder="请输入手机号"
        type="tel"
      ></van-field>
      <van-field
        v-if="isPass"
        v-model="password"
        :rules="passwordRules"
        placeholder="请输入密码"
        type="password"
      ></van-field>
      <van-field v-else :rules="codeRules" placeholder="短信验证码" v-model="code">
        <!-- 具名插槽 -->
        <template #button>
          <span class="btn-send">发送验证码</span>
        </template>
      </van-field>
      <div class="cp-cell">
        <van-checkbox v-model="agree">
          <span>我已同意</span>
          <a href="javascript:;">用户协议</a>
          <span>及</span>
          <a href="javascript:;">隐私条款</a>
        </van-checkbox>
      </div>
      <div class="cp-cell">
        <van-button native-type="submit" block round type="primary"> 登 录 </van-button>
      </div>
      <div class="cp-cell">
        <a href="javascript:;">忘记密码？</a>
      </div>
    </van-form>
    <!-- 底部 -->
    <div class="login-other">
      <van-divider>第三方登录</van-divider>
      <a class="icon">
        <img src="@/assets/qq.svg" alt="" />
      </a>
    </div>
  </div>
</template>

